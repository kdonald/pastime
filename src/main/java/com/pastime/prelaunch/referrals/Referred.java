package com.pastime.prelaunch.referrals;

public class Referred {
    
    private String date;
    
    private String name;

    public Referred(String date, String name) {
        this.date = date;
        this.name = name;
    }

    public String getDate() {
        return date;
    }

    public String getName() {
        return name;
    }
    
}
